// Variable Initialization
var a = 5;
var b = 10;
var c = 'I am a ';
// the same applies to subtraction, division and multiplication
a = a + 1;
console.log(a);
// or
a += 1; // adding value to variable and assigning the output to the variable
console.log(a);
b = b + 5;
console.log(b);
// or
b += 5;
console.log(b);
// start of concatenation
c = c + "String!";
console.log(c);
c += "String!"
console.log(c);
// end of concatenation
// Variables here are case-sensitive
var myName;
var MyName;

myName = 'Charlie';
console.log(myName);
MyName = 'Ben';
console.log(MyName);

var myStr = 'I am a "Double Quoted" quote inside a string';
console.log(myStr);
// using escape
var myStr2 = "I am a \"Double Quoted\" quote inside a string";
console.log(myStr2);